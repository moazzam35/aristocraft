function RenderContent({ children }: { children: React.ReactNode }) {
  return (
    <div className="space-y-4">
      {React.Children.map(children, (child) => {
        if (!React.isValidElement(child)) return null;
        const el = child as React.ReactElement<{ children?: React.ReactNode }>;
        if (el.type === "p") {
          return (
            <p className="text-sm leading-7" style={{ fontWeight: 300, color: "#111111" }}>
              {el.props.children}
            </p>
          );
        }
        if (el.type === "ul") {
          return (
            <ul className="space-y-2.5 py-1">
              {React.Children.map(el.props.children, (li) => {
                if (!React.isValidElement(li)) return null;
                const liEl = li as React.ReactElement<{ children?: React.ReactNode }>;
                return (
                  <li className="flex items-start gap-3 text-sm leading-7" style={{ color: "#111111" }}>
                    <span className="mt-[11px] w-[5px] h-[5px] rounded-full bg-[#C9A96E] shrink-0" aria-hidden="true" />
                    <span style={{ fontWeight: 300 }}>{liEl.props.children}</span>
                  </li>
                );
              })}
            </ul>
          );
        }
        return child;
      })}
    </div>
  );
}

